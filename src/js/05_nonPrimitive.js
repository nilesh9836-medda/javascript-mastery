// Datatype Object: Represents key-value pairs
let obj = {
    name: "Nilesh",
    age: 23
};
console.log(obj);

// Datatype Array: Represents an ordered list of values
let a = ["A", "B", "C"];
console.log(a);

// Datatype Function: Represents reusable blocks of code
function myFunc() {
    console.log("JavaScript Mastering");
}
console.log(myFunc);
myFunc();
